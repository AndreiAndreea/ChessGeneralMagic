#pragma once
/**
 * @brief Enumeration of players.
 *
 * This enum class defines different players in a chess game, including the white player,
 * black player, and a "None" option to represent a neutral or undefined player.
 * 
 * 
 */
enum class EPlayer {
    White,      /**< The white player in the chess game. */
    Black,      /**< The black player in the chess game. */
    None        /**< Represents a neutral or undefined player. */
};